# 预设提示词映射
PRESET_PROMPTS = {
    "温和型导师": DEFAULT_PROMPT + "\n\n" + GENTLE_STYLE,
    "正常型导师（默认）": DEFAULT_PROMPT + "\n\n" + NORMAL_STYLE,
    "压力型导师": DEFAULT_PROMPT + "\n\n" + PRESSURE_STYLE,
    "自定义": "",
}

# 别名映射
PROMPT_ALIASES = {
    "温和型": "温和型导师",
    "正常型（默认）": "正常型导师（默认）",
    "正常型导师": "正常型导师（默认）",
    "压力型": "压力型导师",
}


def normalize_prompt_choice(choice: Optional[str]) -> str:
    """标准化提示词选择"""
    legacy_role = "面试" + "官"
    normalized = (choice or "").strip().replace(legacy_role, "导师")
    normalized = PROMPT_ALIASES.get(normalized, normalized)
    if normalized in PRESET_PROMPTS:
        return normalized
    return "正常型导师（默认）"


def get_preset_prompt(choice: Optional[str]) -> str:
    """获取预设提示词"""
    normalized = normalize_prompt_choice(choice)
    return PRESET_PROMPTS.get(normalized, "")


